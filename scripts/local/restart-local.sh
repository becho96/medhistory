SERVICES=""

while [[ $# -gt 0 ]]; do
    case $1 in
        backend|be)
            SERVICES="$SERVICES backend"
            shift
            ;;
        frontend|fe)
            SERVICES="$SERVICES frontend"
            shift
            ;;
        all|*)
            SERVICES="backend frontend"
            break
            ;;
    esac
done

# –ï—Å–ª–∏ –Ω–µ —É–∫–∞–∑–∞–Ω—ã —Å–µ—Ä–≤–∏—Å—ã, –ø–µ—Ä–µ–∑–∞–ø—É—Å–∫–∞–µ–º –≤—Å—ë
if [ -z "$SERVICES" ]; then
    SERVICES="backend frontend"
fi

echo "üì¶ –ü–µ—Ä–µ–∑–∞–ø—É—Å–∫–∞–µ–º: $SERVICES"
echo ""

# –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Å–µ—Ä–≤–∏—Å—ã
echo "‚èπÔ∏è  –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä—ã..."
docker compose stop $SERVICES

# –ü–µ—Ä–µ—Å–æ–±–∏—Ä–∞–µ–º –æ–±—Ä–∞–∑—ã (–Ω–∞ —Å–ª—É—á–∞–π –∏–∑–º–µ–Ω–µ–Ω–∏–π –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç—è—Ö)
echo ""
echo "üî® –ü–µ—Ä–µ—Å–æ–±–∏—Ä–∞–µ–º –æ–±—Ä–∞–∑—ã..."
