#!/bin/bash

# MedHistory Local Restart Script
# –ò—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –ø–æ—Å–ª–µ –∏–∑–º–µ–Ω–µ–Ω–∏–π –≤ –∫–æ–¥–µ –¥–ª—è –ø—Ä–∏–º–µ–Ω–µ–Ω–∏—è –∏–∑–º–µ–Ω–µ–Ω–∏–π –≤ Docker

echo "üîÑ –ü–µ—Ä–µ–∑–∞–ø—É—Å–∫ MedHistory (–ª–æ–∫–∞–ª—å–Ω—ã–π Docker)..."
